from murdle_solver.solutions import gen_solutions


def test_solutions_two() -> None:
    """Test the solutions generation."""
    groups = [["a", "b"], ["c", "d"]]
    assert list(gen_solutions(groups)) == [
        (("a", "c"), ("b", "d")),
        (("a", "d"), ("b", "c")),
    ]


